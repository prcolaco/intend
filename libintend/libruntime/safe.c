/*
 * Intend C Safe Mode control in runtime
 */

#include <stdlib.h>
#include <string.h>

#include "runtime.h"

/*
 * Set global safe mode vars
 */
void safe_mode_vars(intend_state *state)
{
    register_variable(state, "SAFE_MODE", VALUE_TYPE_BOOL, &state->safe_mode);
}

/*
 * Set safe mode on or off
 */
void safe_mode_set(intend_state *state, int safe_mode)
{
    state->safe_mode = safe_mode;
}

/*
 * Get safe mode from state
 */
int safe_mode_get(intend_state *state)
{
    return state->safe_mode;
}

